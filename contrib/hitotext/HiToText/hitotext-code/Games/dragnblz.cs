        public string ByteArrayToString(byte[] data)
        {
            StringBuilder sb = new StringBuilder();

            for (int i = 0; i < data.Length; i++)
            {
                if (data[i] >= 0x00 && data[i] <= 0x19)
                    sb.Append(((char)(((int)data[i]) + 65)));
                else if (data[i] == 0x24)
                    sb.Append(',');
                else if (data[i] == 0x25)
                    sb.Append('~');
                else if (data[i] == 0x26)
                    sb.Append('.');
                else if (data[i] == 0x27)
                    sb.Append('/');
                else if (data[i] == 0x28)
                    sb.Append('[');
                else if (data[i] == 0x29)
                    sb.Append(']');
                else if (data[i] == 0xff)
                    sb.Append(' ');
            }

            return sb.ToString();
        }

        public byte[] StringToByteArray(string str)
        {
            byte[] data = new byte[str.Length];

            for (int i = 0; i < str.Length; i++)
            {
                if (str[i] >= 'A' && str[i] <= 'Z')
                    data[i] = (byte)((int)str[i] - 65);
                else if (str[i] == ',')
                    data[i] = 0x24;
                else if (str[i] == '~')
                    data[i] = 0x25;
                else if (str[i] == '.')
                    data[i] = 0x26;
                else if (str[i] == '/')
                    data[i] = 0x27;
                else if (str[i] == '[')
                    data[i] = 0x28;
                else if (str[i] == ']')
                    data[i] = 0x29;
                else if (str[i] == ' ')
                    data[i] = 0xff;
            }

            return data;
        }

