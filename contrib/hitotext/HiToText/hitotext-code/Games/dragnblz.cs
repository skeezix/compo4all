        private String GetLevel(int level)
        {
            //Levels are 1-1 to 1-7, 2-1 to 2-7, then ALL (0x0e)
            if (level == 14)
                return "ALL";
            else if (level > 6)
                return "2-" + (level - 6).ToString();
            else
                return "1-" + (level + 1).ToString();
        }

        private int GetLevel(String level)
        {
            //Levels are 1-1 to 1-7, 2-1 to 2-7, then ALL (0x0e)
            if (level.Equals("ALL"))
                return 14;
            else if (level.Substring(0, 1).Equals("1"))
                return Convert.ToInt32(level.Substring(level.Length - 1)) - 1;
            else
                return Convert.ToInt32(level.Substring(level.Length - 1)) + 6;
        }

