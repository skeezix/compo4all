        public qbertqub()
        {
            // Qberqub has a checksum in the score tables, but the first one (Noser Elite) is always reset
            // when you run the machine. It does not matter that this score table and the checksum is
            // correctly saved in the nvram. The routine is placed at 0xec0d4. 
            m_numEntries = 20;
            m_format = "RANK|SCORE|NAME";
            m_gamesSupported = "qbertqub";
            m_extensionsRequired = ".nv";
        }

        public string ByteArrayToString(byte[] data)
        {
            StringBuilder sb = new StringBuilder();

            for (int i = 0; i < data.Length; i++)
            {
                if (data[i] >= 0x0a && data[i] <= 0x23)
                    sb.Append(((char)((((int)data[i])) + 65 - 0x0a)));
                else if (data[i] == 0xff)
                    sb.Append(' ');
            }

            return sb.ToString();
        }

        public byte[] StringToByteArray(string str)
        {
            byte[] data = new byte[str.Length];

            for (int i = 0; i < str.Length; i++)
            {
                if (str[i] >= 'A' && str[i] <= 'Z')
                    data[i] = (byte)(((int)str[i] - 65 + 0x0a));
                else if (str[i] == ' ')
                    data[i] = 0xff;                             
            }
            return data;
        }

        public byte[] CalculateChecksum(byte[] data)
        {
            int sum = 0x0000;
            int checkstart = 0x032f; // First byte to consider
            int checkend = 0x045b; // Last byte included in the checksum]           

            for (int i = checkstart; i < checkend; i+=2)
            {
                sum += (int)(data[i] + (data[i+1] * 256));
                if (sum >= 0x010000) sum = sum - 0x010000;
            }
            string strsum = sum.ToString("X2");
            data[0x045c] = Convert.ToByte(strsum.Substring(2, 2), 0x10); //0x10 -> Convert from hex number
            data[0x045d] = Convert.ToByte(strsum.Substring(0, 2), 0x10);
            
            return data;
        }

